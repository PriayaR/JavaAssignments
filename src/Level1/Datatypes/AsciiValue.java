package Level1.Datatypes;

public class AsciiValue {
    public static void main(String[] args) {

        //we just assign character to an int variable to find its ascii value
        int c1= 'a';
        int c2= 'z';

        //Printing the ASCII value of a character
        System.out.println("ASCII value of c1:" +c1);
        System.out.println("ASCII value of c2:" +c2);
    }
}
